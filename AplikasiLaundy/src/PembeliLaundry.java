    private void buttonpesansekarangpebeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonpesansekarangpebeliActionPerformed
        // TODO add your handling code here:
       
        String val_id_pembeli = idpembeli.getText();
        String val_nama = namapembeli.getText();
        String val_totalBerat = totalberatpembeli.getText();
        String val_alamat = alamatpembeli.getText();
        String val_tanggalPesan = tanggalpesanpembeli.getText();
        
        
        StringBuilder val_jenisbajuBuilder = new StringBuilder();
        if (baju.isSelected()) val_jenisbajuBuilder.append("Baju, ");
        if (seprai.isSelected()) val_jenisbajuBuilder.append("Seprai, ");
        if (selimut.isSelected()) val_jenisbajuBuilder.append("Selimut, ");
        if (boneka.isSelected()) val_jenisbajuBuilder.append("Boneka, ");
        if (bajubahankhusus.isSelected()) val_jenisbajuBuilder.append("Baju Bahan Khusus, ");
        String val_jenis_baju = val_jenisbajuBuilder.toString().replaceAll(", $", ""); // pastikan string sesuai dengan kolom

        
        String val_jenisLayanan;
        if (cucikering.isSelected()) {
            val_jenisLayanan = "Cuci Kering";
        } else if (cucibasah.isSelected()) {
            val_jenisLayanan = "Cuci Basah";
        } else if (ketiganya.isSelected()) {
            val_jenisLayanan = "Cuci Kering, Cuci Basah, Setrika";
        } else {
            val_jenisLayanan = ""; 
        }

        String val_jenisPaket;
        if (reguler.isSelected()) {
            val_jenisPaket = "Reguler";
        } else if (highquality.isSelected()) {
            val_jenisPaket = "High Quality";
        } else {
            val_jenisPaket = ""; // Jika tidak dipilih, beri nilai kosong
        }        
      
        
       
        BagianPembeli brg = new BagianPembeli(
            0,
            Integer.parseInt(val_totalBerat),
            val_nama,
            val_alamat,
            val_tanggalPesan,
            val_jenis_baju,
            val_jenisLayanan,
            val_jenisPaket
        );

        try {
            Pemesanan.InsertPembeli(brg);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Tidak dapat menambahkan data, error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonpesansekarangpebeliActionPerformed


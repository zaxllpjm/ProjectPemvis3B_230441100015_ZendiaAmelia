    private void editpembeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editpembeliActionPerformed
        // TODO add your handling code here:
        try {
            String nama = namapembeli.getText();

            if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nama pemesan harus diisi jika ingin mengedit Pesanan.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!isEditing) {
                BagianPembeli pemesanan = Pemesanan.getPesanan(nama);

                if (pemesanan != null) {
                    int id = pemesanan.getId();
                    namapembeli.setText(pemesanan.getNamaPembeli());
                    totalberatpembeli.setText(String.valueOf(pemesanan.getTotalBerat()));
                    alamatpembeli.setText(pemesanan.getAlamat());
                    tanggalpesanpembeli.setText(pemesanan.getTanggalPesan());
                    
                    String[] jenisBajuArray = pemesanan.getJenisBaju().split(", ");
                    baju.setSelected(Arrays.asList(jenisBajuArray).contains("Baju"));
                    seprai.setSelected(Arrays.asList(jenisBajuArray).contains("Seprai"));
                    selimut.setSelected(Arrays.asList(jenisBajuArray).contains("Selimut"));
                    boneka.setSelected(Arrays.asList(jenisBajuArray).contains("Boneka"));
                    bajubahankhusus.setSelected(Arrays.asList(jenisBajuArray).contains("Baju Bahan Khusus"));

                    cucikering.setSelected("Cuci Kering".equals(pemesanan.getJenisLayanan()));
                    cucibasah.setSelected("Cuci Basah".equals(pemesanan.getJenisLayanan()));
                    ketiganya.setSelected("Cuci Kering, Cuci Basah, Setrika".equals(pemesanan.getJenisLayanan()));

                    highquality.setSelected("High Quality".equals(pemesanan.getJenisPaket()));
                    reguler.setSelected("Reguler".equals(pemesanan.getJenisPaket()));

                    isEditing = true;
                    JOptionPane.showMessageDialog(null, "Data ditemukan. Silakan edit dan klik tombol Edit lagi untuk menyimpan perubahan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan untuk nama pemesan: " + nama, "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
     

                String nama_pembeli = namapembeli.getText();
                int total_berat = Integer.parseInt(totalberatpembeli.getText());
                String alamat = alamatpembeli.getText();
                String tgl_pesan = tanggalpesanpembeli.getText();

                String jenis_baju = "";
                if (baju.isSelected()) {
                    jenis_baju = "Baju";
                } else if (seprai.isSelected()) {
                    jenis_baju = "Seprai";
                } else if (selimut.isSelected()) {
                    jenis_baju = "Selimut";
                } else if (boneka.isSelected()) {
                    jenis_baju = "Boneka";
                } else if (bajubahankhusus.isSelected()) {
                    jenis_baju = "Baju Bahan Khusus";
                }

                if (jenis_baju.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Silakan pilih Jenis Baju Anda.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                String jenis_layanan = "";
                if (cucikering.isSelected()) jenis_layanan = "Cuci Kering";
                else if (cucibasah.isSelected()) jenis_layanan = "Cuci Basah";
                else if (ketiganya.isSelected()) jenis_layanan = "Cuci Kering, Cuci Basah, Setrika";

                String jenis_paket = highquality.isSelected() ? "High Quality" : "Reguler";

                BagianPembeli pemesanan = new BagianPembeli (id, total_berat, nama_pembeli, alamat, tgl_pesan, jenis_baju, jenis_layanan, jenis_paket);
                Pemesanan.updatePembeli(pemesanan);

                JOptionPane.showMessageDialog(null, "Data berhasil diubah.", "Informasi", JOptionPane.INFORMATION_MESSAGE);

                isEditing = false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Data tidak valid: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editpembeliActionPerformed

